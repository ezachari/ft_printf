#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*s;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(str);
	if (!(s = malloc(len + 1)))
		return (NULL);
	while (str[i] != '\0')
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
